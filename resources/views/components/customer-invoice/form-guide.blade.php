<x-module.form-guide subtitle="Monthly fleet billing">
    <section class="mi-guide-section">
        <h3 class="mi-guide-section-title">
            <i class="fas fa-file-invoice-dollar"></i> What gets invoiced?
        </h3>
        <p class="mi-guide-text">
            An invoice bundles all <strong>uninvoiced credit sales</strong> for a fleet account within the selected date range. Each sale was already issued on account at the cash desk.
        </p>
    </section>

    <section class="mi-guide-section">
        <h3 class="mi-guide-section-title">
            <i class="fas fa-route"></i> Steps
        </h3>
        <ul class="mi-guide-list">
            <li><strong>Select account</strong><span>Choose the fleet customer to bill.</span></li>
            <li><strong>Set period</strong><span>Typically the full calendar month — adjust if needed.</span></li>
            <li><strong>Review preview</strong><span>Confirm the sales list and total before generating.</span></li>
            <li><strong>Generate</strong><span>Creates the invoice and links all listed sales to it.</span></li>
            <li><strong>Print &amp; collect</strong><span>Print the statement and record payment when received.</span></li>
        </ul>
    </section>

    <section class="mi-guide-section">
        <h3 class="mi-guide-section-title">
            <i class="fas fa-lightbulb"></i> Tips
        </h3>
        <ul class="mi-guide-tips">
            <li><i class="fas fa-check"></i> Run invoices at month-end after all credit sales for the period are complete.</li>
            <li><i class="fas fa-check"></i> Only sales not yet linked to an invoice appear in the preview.</li>
            <li><i class="fas fa-check"></i> You can add notes on the invoice for special payment instructions.</li>
            <li><i class="fas fa-check"></i> Partial payments are supported — record each lump payment as received.</li>
        </ul>
    </section>

    <div class="mi-guide-note mi-guide-note-blue">
        <i class="fas fa-arrow-right"></i>
        <p>After generating, open the invoice to print the statement and record payment from the fleet operator.</p>
    </div>
</x-module.form-guide>
