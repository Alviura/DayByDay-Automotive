    <section class="mi-guide-section mi-guide-section-first">
        <h3 class="mi-guide-section-title">
            <i class="fas fa-clipboard-list"></i> Your role
        </h3>
        <p class="mi-guide-text">
            As shop attendant, you build the customer's order and send it to the cash desk. You do not take payment or negotiate price.
        </p>
    </section>

    <section class="mi-guide-section">
        <h3 class="mi-guide-section-title">
            <i class="fas fa-route"></i> Workflow
        </h3>
        <ul class="mi-guide-list">
            <li><strong>Search parts</strong><span>Find products by part number or name. Stock shown is for this shop.</span></li>
            <li><strong>Build the cart</strong><span>Add lines and adjust quantities. List price is an estimate only.</span></li>
            <li><strong>Customer details</strong><span>Name, phone, and notes are optional but help the cashier.</span></li>
            <li><strong>Send to desk</strong><span>Order moves to the cash desk queue and stock is reserved.</span></li>
        </ul>
    </section>

    <section class="mi-guide-section">
        <h3 class="mi-guide-section-title">
            <i class="fas fa-tag"></i> Pricing
        </h3>
        <p class="mi-guide-text">
            Retail orders use list (max) price as an estimate. Fleet mode uses the minimum selling price per product. The shop manager can still adjust within the allowed range at the cash desk.
        </p>
    </section>

    <section class="mi-guide-section">
        <h3 class="mi-guide-section-title">
            <i class="fas fa-lightbulb"></i> Tips
        </h3>
        <ul class="mi-guide-tips">
            <li><i class="fas fa-check"></i> Type at least 2 characters to search.</li>
            <li><i class="fas fa-check"></i> Add notes if the customer wants a specific price range discussed at the desk.</li>
            <li><i class="fas fa-check"></i> Walk-in customers can be left blank — the cashier will confirm details.</li>
        </ul>
    </section>

    <div class="mi-guide-note mi-guide-note-amber">
